main() {
  print("int: 7 + 2 = ${7 + 2}"); // 9
  print("int: 7 - 2 = ${7 - 2}"); // 5
  print("int: 7 * 2 = ${7 * 2}"); // 14
  print("int: 7 / 2 = ${7 / 2} (double)"); // 3.5 (double)
  print("int: 7 ~/ 2 = ${7 ~/ 2} (int)"); // 3 (int)
  print("int: 7 % 2 = ${7 % 2}"); // 1
}
