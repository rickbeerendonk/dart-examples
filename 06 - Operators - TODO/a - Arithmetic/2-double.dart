void main() {
  print("Double: 7.0 + 2.0: ${7.0 + 2.0}");  // 9.0
  print("Double: 7.0 - 2.0: ${7.0 - 2.0}");  // 5.0
  print("Double: 7.0 * 2.0: ${7.0 * 2.0}");  // 14.0
  print("Double: 7.0 / 2.0: ${7.0 / 2.0}");  // 3.5
  print("Double: 7.0 % 2.0: ${7.0 % 2.0}");  // 1.0
}
